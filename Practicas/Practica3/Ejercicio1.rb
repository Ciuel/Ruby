# Investigá la jerarquía de clases que presenta Ruby para las excepciones. ¿Para qué se utilizan las siguientes clases?
#
# ArgumentError
# IOError
# NameError
# NotImplementedError
# RuntimeError
# StandardError
# StopIteration
# SystemExit
# SystemStackError
# TypeError
# ZeroDivisionError
#
# ArgumentError: Se utiliza cuando se pasa un argumento incorrecto a un método.
# IOError: Se utiliza cuando se produce un error de entrada/salida.
# NameError: Se utiliza cuando se intenta acceder a un nombre que no está definido.
# NotImplementedError: Se utiliza cuando se intenta llamar a un método que no está implementado.
# RuntimeError: Se utiliza cuando se produce un error en tiempo de ejecución.
# StandardError: Se utiliza cuando se produce un error estándar.
# StopIteration: Se utiliza cuando se produce una iteración que no se puede detener.
# SystemExit: Se utiliza cuando se produce una salida del sistema.
# SystemStackError: Se utiliza cuando se produce un error de pila del sistema.
# TypeError: Se utiliza cuando se produce un error de tipo.
# ZeroDivisionError: Se utiliza cuando se produce una división por cero.
